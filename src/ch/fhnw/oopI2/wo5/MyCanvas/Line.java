package ch.fhnw.oopI2.wo5.MyCanvas;

public class Line {

    public int xStart;
    public int yStart;
    public int xEnd;
    public int yEnd;

    public Line(int xStart, int yStart, int xEnd, int yEnd)
    {
        this.xStart = xStart;
        this.yStart = yStart;
        this.xEnd = xEnd;
        this.yEnd = yEnd;
    }
}
